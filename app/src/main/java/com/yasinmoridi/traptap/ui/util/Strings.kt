 // دریافت نام زبان در حالت بومی (Native) برای نمایش در لیست انتخاب زبان
fun getNativeLanguageName(code: String): String {
    return when (code) {
        "fa" -> "فارسی"
        "zh" -> "中文"
        "ru" -> "Русский"
        "de" -> "Deutsch"
        "hi" -> "हिन्दी"
        "ar" -> "العربية"
        "fr" -> "Français"
        "es" -> "Español"
        "he" -> "עברית"
        "tr" -> "Türkçe"
        "vi" -> "Tiếng Việt"
        "pt" -> "Português"
        "ja" -> "日本語"
        "ko" -> "한국어"
        "it" -> "Italiano"
        "id" -> "Bahasa Indonesia"
        "th" -> "ไทย"
        "pl" -> "Polski"
        "nl" -> "Nederlands"
        "uk" -> "Українська"
        "cs" -> "Čeština"
        "ro" -> "Română"
        "hu" -> "Magyar"
        "sv" -> "Svenska"
        else -> "English"
    }
}


